		<h2>What's new in the Windows 10 Technical Preview 2 build 9926?</h2>
		<p>Windows will update from version 6.4.9879 to 10.0.9926 after launching this update.</p>
		<div class="alert alert-success">
			<h4>Re-enabled features</h4>
			Universal scroll is back in this build as optional feature.
		</div>
		<h3>Desktop</h3>
		<ul>
			<li>The desktop has received a new interface</li>
			<li>Some animations have been altered</li>
			<li>Win32 apps now also use gestures</li>
			<li>Some icons have been redesigned to match the new UI</li>
			<li>The search button is replaced with a searchbar by default</li>
			<li>You can now choose the default folder for File Explorer</li>
			<li>Win32 window chromes are now more similar to WinRT apps</li>
			<li>Personalisation
				<ul>
					<li>The window color choices have been changed</li><!-- Ambiguous, what's this? -->
					<li>There are new backgrounds</li>
				</ul>
			</li>
		</ul>
		<h3>Start</h3>
		<ul>
			<li>Continuum is now available</li>
			<li>The Start grid has been improved</li>
			<li>The Start screen grid now scrolls vertically</li>
			<li>Tiles no longer use a gradient and border</li>
		</ul>
		<h3>Features</h3>
		<ul>
			<li>Action Center
				<ul>
					<li>The Action Center has been revamped</li>
					<li>You can now change quick settings in the Action Center</li>
					<li>Notifications now can contain actions</li>
					<li>A swipe from the right now opens the Action Center</li>
					<li>Quick settings can now contain more settings</li>
					<li>Notifications have received a new design</li>
				</ul>
			</li>
			<li>It's easier to connect to wireless audio and video devices</li>
			<li>ALT+TAB now opens a view that's merged with the Task View</li>
			<li>Classic Calculator has been removed</li>
			<li>A "Pin to quick access" options has been added to the ribbon in the File Explorer, replacing Favorites</li>
		</ul>
		<h3>Internet Explorer</h3>
		<ul>
			<li>The Edge 12 rendering engine
				<ul>
					<li>HTTP Strict Transport Security (HSTS)</li>
					<li>HTTP Live Streaming (HLS)</li>
					<li>Video Tracks</li>
					<li>DOM L3 XPath</li>
					<li>Improved ECMAScript 6 Standard support</li>
					<li>Improved ECMAScript non-standard support</li>
				</ul>
			</li>
			<li>F12 developer tools
				<ul>
					<li>New and improved Network Tools</li>
					<li>HTML and CSS Pretty Printing support</li>
					<li>Async Callstacks for Events and Timers</li>
					<li>Sourcemaps for Styles and in the Memory Profiler</li>
					<li>Find references and Go To Definitions</li>
				</ul>
			</li>
			<li>More Windows Insiders will get Edge 12 as the default engine to replace Trident 7</li>
		</ul>
		<h3>WinRT</h3>
		<ul>
			<li>WinRT apps have a new border chrome</li>
			<li>Full screen mode is now a button on the window instead of in the hamburger menu</li>
			<li>Settings fly-out now appears within an app</li>
			<li>The Charms bar has been removed</li>
			<li>Apps can now run within File Explorer (or pick a photo, Photos will load in File Explorer, for example)</li>
		</ul>
		<h3>Cortana</h3>
		<ul>
			<li>Cortana has been added</li>
		</ul>
