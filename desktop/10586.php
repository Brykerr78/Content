<h3>Settings</h3>
<ul>
<li>The Settings-icon on the taskbar has no longer a background color</li>
<li>The Settings-tile is now transparent for non-fresh installs too</li>
<li>The Settings app's splash screen now uses the accent color of the user</li>
<li>The About page now shows the Windows 10 logo in the user's accent color</li>
<li>Installing apps to another drive has been enabled again</li>
<li>You can now change where Windows has to store offline maps</li>
