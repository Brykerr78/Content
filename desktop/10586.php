<h3>Start</h3>
<ul>
<li>You can now show either 3 or 4 medium tiles per group</li>
<li>Tiles now can have jumplists</li>
<li>The start context menus have been updated to display icons and contain the app's jumplist</li>
<li>Cortana now also works for local accounts</li>
<li>The context menus for tiles have been reorganized</li>
<ul>
<li>You can now share apps through this menu</li>
<li>You can now review apps through this menu</li>
</ul>
<li>Desktop app tiles now can have their own color (Office 2013, Firefox, etc)</li>
<li>Start now supports up to 2048 tiles (up from 512)</li>
</ul>
<h3>Interface</h3>
<ul>
<li>The title bar of WinRT apps will now take the color of the splash screen while starting</li>
<li>Context menus have a more consistent look</li>
<li>When colors are set to the taskbar, start and action center, they now also apply to title bars</li>
<li>The look for dropdowns has been changed system wide</li>
<li>Jumplists have a new design</li>
<li>Improved dark and light theming</li>
<li>The text that&nbsp;is shown while preparing your account after an upgrade&nbsp;has been updated</li>
</ul>
<h3>Desktop</h3>
<ul>
<li>When you snap a window and select another window to snap beside it, you can resize both windows at once (only horizontal)</li>
<ul>
<li>This needs to be enabled under System &gt; Multitasking</li>
</ul>
</ul>
<h3>Tablet mode</h3>
<ul>
