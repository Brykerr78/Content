<li>You can now locate your device with GPS and Location tracking in "Find my device"</li>
<li>The Windows Insider Ring now has to be set with a slider instead of a dropdown</li>
<li>The Activation-screen no longer shows a product ID and key but the status if you've been digitally entitled</li>
<li>You can now (dis)allow apps to access your call history</li>
<li>You can now (dis)allow apps to access and send emails</li>
<li>You can now make Windows select the correct time zone automatically</li>
<li>The "Storage" tab now has icons for each file format</li>
<li>You can let Windows notify you when there are issues with any USB-connected device</li>
<li>The Insider settings now show your account details and allow you to stop receiving builds for a limited amount of days</li>
<li>The Control Panel's Theme Settings have been extended, back to the state from Windows 8.1</li>
