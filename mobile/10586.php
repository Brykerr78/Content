<h2>What's new in Windows 10 Mobile build 10586.0?</h2>
<p>This is in comparison with Windows Phone 8.1 Update 2 build 9651.</p>
<h3>Start</h3>
<ul>
<li>There are multiple new live tile animations</li>
<li>Wallpapers are now shown behind the tiles instead of in them</li>
<li>Recent installed apps are now shown at the top of the app list</li>
<li>The search button in "All apps" has been replaced with a search box</li>
<li>The "All apps" lists jumplist has received a small update</li>
</ul>
<h3>Interface</h3>
<ul>
<li>New context menu design</li>
<li>App Switcher</li>
<ul>
<li>The app switcher has a new design</li>
<li>It will now show 4 apps per page on large devices</li>
<li>The orientation has been shifted to left to right</li>
</ul>
<li>Cortana has a redesigned interface</li>
<li>The tile layout for Kids Corner has been changed</li>
<li>There are new wallpapers available, dated April 1975</li>
</ul>
<h3>Features</h3>
<ul>
<li>Action Center</li>
<ul>
<li>Notifications can now be expanded and contain actions</li>
<li>Quick settings have been extended with more toggles</li>
<li>Notifications now get synced between devices</li>
<li>Quick settings now switch on or off their respective setting, pressing them will bring you to the settings app</li>
<li>New quick settings for Flashlights</li>
</ul>
<li>When the device is turned off, it will remind you of your next appointment</li>
<li>Cortana</li>
<ul>
<li>Updated notebook</li>
<li>You can now set up quiet hours in the notebook</li>
<li>Cortana is now available in Japan</li>
<li>Cortana is now available in Australia and Canada in English</li>
<li>Cortana can now understand your inked notes</li>
<li>Cortana can now track you movies and ticketed events</li>
<li>You can now send text messages with your PC from Cortana</li>
<li>Cortana will now alert you on your PC when you've a missed call</li>
<li>Integration with Uber</li>
</ul>
<li>You can now encrypt your device</li>
<li>Keyboard&nbsp;</li>
<ul>
<li>The keyboard can be made smaller for one-handed usage</li>
<li>The keyboard now has a pointing stick</li>
<li>You can now change the language of the keyboard by swiping over the space bar</li>
<li>You can now use the top row to add numbers</li>
</ul>
<li>More fields now support speech input</li>
<li>You can now disconnect your Microsoft Account</li>
<li>You can now set app permissions for location</li>
<li>You can now log in with Windows Hello (if your device supports Windows Hello)</li>
<li>You can now connect your device to a screen, mouse and keyboard and use it as a full PC (if your device supports Continuum)</li>
<li>Emoji&rsquo;s are now diverse</li>
<li>Pressing and holding the Windows-button will make it interface come down</li>
</ul>
<h3>Microsoft Edge</h3>
<ul>
<li>Internet Explorer 11 has been replaced with Microsoft Edge 13 (app version 25)</li>
<li>Reading List is now integrated in Edge</li>
<li>Favorites and Reading List are now synced with other devices</li>
<li>New download management</li>
<li>New settings menu</li>
<li>You can now enable experimental EdgeHTML features</li>
</ul>
<h3>Settings</h3>
<ul>
<li>Settings has been fully redesigned</li>
<li>You can now set 3G-only has highest connection speed</li>
<li>You now get to see a splitted interface in landscape mode on larger devices</li>
<li>Make device visible for USB Ethernet Emulation Model</li>
<li>Make device show up trough USB connection and in local network</li>
<li>You can now change the text size of the OS</li>
<li>"Device Portal" is now available under Developer settings</li>
<li>You can now set which apps have access to your call history</li>
<li>The keyboard size can now be altered</li>
<li>You can now choose where maps should be saved</li>
<li>Storage settings have been updated</li>
<li>You can now defer updates</li>
</ul>
<h3>Security</h3>
<ul>
<li>You can now reset your PIN after 5 failed logins through your Microsoft Account</li>
<li>After failing to many attempts on a PIN, you'll now get a prompt to type "A1B2C3" instead of being locked out for a given time</li>
</ul>
<h3>System</h3>
<ul>
<li>Support for notifications trough LEDs</li>
<li>Support for pen and inking</li>
<li>Support for pointer devices</li>
<li>Both VPN Point to Point Tunneling Protocol (PPTP) and Secure Socket Tunneling Protocol (SSTP) are now supported</li>
<li>Video stabilization</li>
<li>Support for Japanese and English (India) voice input</li>
</ul>
<h3>Apps</h3>
<ul>
<li>All previously build-in apps have been redesigned to match the Windows 10 Design Language</li>
<li>Mail has been replaced by Outlook Mail</li>
<li>Calendar has been replaced by Outlook Calendar</li>
<li>Xbox Music has been replaced with Groove</li>
<li>Xbox Video has been replaced with Movies &amp;&nbsp;TV</li>
<li>Windows Phone Store has been replaced with Windows Store</li>
<li>Office Mobile has been replaced with Word Mobile, Excel Mobile and PowerPoint Mobile</li>
<li>File Explorer has been added as a default app</li>
<li>Skype Video has been added as a default app</li>
<li>Windows Feedback has been added as a default app</li>
<li>Contact Support has been added as a default app</li>
<li>Get Started has been added as a default app</li>
<li>MSN Health &amp; Fitness and&nbsp;MSN Travel are no longer default apps</li>
</ul>
