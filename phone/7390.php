<h2>What's new in Windows Phone 7 build 7390.0?</h2>
<h3>Features</h3>
<ul>
<li>There is now support for Copy and Paste</li>
<li>You can now resume playing music after watching a video</li>
<li>Integration with Facebook has been improved</li>
</ul>
<h3>Apps</h3>
<ul>
<li>Marketplace search has been updated to be context oriented</li>
<li>Improvements when downloading apps and upgrading from trials to full versions, sharing links to apps and more</li>
<li>Outlook has been improved for a better experience when viewing iPhone photo attachments</li>
<li>The settings app will now also show your MAC address</li>
<li>Improved stability when switching between camera and video mode in the camera app</li>
</ul>
<h3>System</h3>
<ul>
<li>Support for CDMA</li>
<li>You can now set an unlimited amount of WiFi profiles and performance has been improved if you've stored multiple profiles</li>
<li>Improved support for Bluetooth headsets when making calls while playing music or videos</li>
<li>Improved performance when starting an app</li>
</ul>
