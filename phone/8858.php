<h2>What's new in Windows Phone 7.8 build 8858.136?</h2>
<h3>Start</h3>
<ul>
<li>The right side bar has been dropped and the start screen now takes the full width</li>
<li>The small tile size has been introduced</li>
<li>Users can now freely resize tiles, the wide tile is only available if the app supports it</li>
</ul>
<h3>Interface</h3>
<ul>
<li>There are now 20 accent colors (though carriers and OEMs can add even more)</li>
<li>The lock screen has been redesiged to match the Windows Phone 8 version</li>
<li>Multiple logos have been updated to match the 2011/2012 brand update within Microsoft</li>
</ul>
<h3>Apps</h3>
<ul>
<li>Support for new countries and regios</li>
</ul>
<h3>System</h3>
<ul>
<li>Improved support for Chinese fonts</li>
<li>Updated Arabic font</li>
</ul>
