<h2>What's new in Windows Phone 8 build 9903?</h2>
<h3>Start</h3>
<ul>
<li>The right side bar has been dropped and the start screen now takes the full width</li>
<li>The small tile size has been introduced</li>
<li>Users can now freely resize tiles, the wide tile is only available if the app supports it</li>
</ul>
<h3>Interface</h3>
<ul>
<li>There are now 20 accent colors (though carriers and OEMs can add even more)</li>
<li>The lock screen has been redesiged with new functionality</li>
<li>Multiple logos have been updated to match the 2011/2012 brand update within Microsoft</li>
</ul>
<h3>Features</h3>
<ul>
<li>Internet Explorer has been updated to version 10 with improved support for HTML5 and CSS3</li>
<li>Improved background multitasking</li>
<li>Navteq replaced Bing technologies for mapping</li>
<li>Native Bitlocker encryption</li>
<li>Manage devices remotly like with Windows</li>
<li>VoIP and video chat integration in Phone and People hub</li>
<li>Support for taking screenshots</li>
<li>Improved SkyDrive integration to sync more data</li>
<li>Data Sense tracking to improve data usage to the situation of the device</li>
<li>Kids corner, a separate environment with its own start screen</li>
<li>Rooms in the People hub</li>
<li>Live apps</li>
<li>Camera</li>
<ul>
<li>Lenses&nbsp;(like a barcode scanner, etc.)</li>
<li>Burst mode to make multiple pictures at once and select the best</li>
<li>Build-in Photosynth technology</li>
</ul>
</ul>
<h3>Apps</h3>
<ul>
<li>Support for in-app purchases</li>
</ul>
<h3>System technologies</h3>
<ul>
<li>The Windows CE 7 kernel has been replaced with Windows NT 6.2</li>
<li>Update over-the-air</li>
<li>Support for multi-core CPUs with a max of 64 cores</li>
<li>Support for 1280x720 and 1280x768 screens</li>
<li>Support for MicroSD cards</li>
<li>Support for Secure Boot</li>
<li>Finished support for NFC (as the Tango update already added this partially)</li>
<li>Support for C and C++ apps</li>
<li>Simplified porting of Windows 8 apps</li>
</ul>