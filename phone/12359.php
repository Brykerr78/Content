<h2>What's new in Windows Phone 8.1 build 12359.845?</h2>
<h3>Interface</h3>
<ul>
<li>The default start screen has been updated for new devices</li>
<li>You can now set a background for your tiles</li>
<li>The lock screen can now be replaced by apps</li>
<li>The app list now shows games too</li>
<li>New Windows Phone-splashscreen</li>
</ul>
<h3>Features</h3>
<ul>
<li>You can now swipe down from the top edge to open the action center</li>
<li>You can now swipe down from the top edge to open the quick actions</li>
<li>Search has been replaced by Cortana in some locaitons</li>
<li>All phones can now set 3 collumns of medium tiles next to each other</li>
<li>Silent hours (only when Cortana is enabled)</li>
<li>Volume has been separated between apps and notifications</li>
<li>The app switcher now supports the ability to close apps by swyping down</li>
<li>The start screen can now be saved in a back-up and synced with other devices</li>
<li>The keyboard has several new features like swype</li>
</ul>
<h3>Internet Explorer</h3>
<ul>
<li>Support to save passwords</li>
<li>File upload support</li>
<li>Tabs are now shown in the app switcher</li>
<li>Support for HTML5-video on YouTube</li>
<li>Trident 7 render engine with improved HTML, CSS and JavaScript support</li>
</ul>
<h3>Store</h3>
<ul>
<li>The store will now provide suggestions</li>
<li>Users can now force the store to check for updates</li>
<li>Apps can now be updated automatically, with an option to do so only over WiFi</li>
<li>Single sign-in support for apps using a Microsoft Account</li>
<li>The Windows Phone Store has been redesigned
<ul>
<li>Screenshots are now shown on the overview</li>
<li>Available languages, version and latest update now has its own page</li>
<li>There is now a graph to indicate ratings</li>
<li>Shortcuts to Home, Share, My apps, Settings and Downloads have been added in the ellipse menu</li>
<li>There is a new "Category"-section</li>
</ul>
</li>
</ul>
<h3>Apps</h3>
<ul>
<li>Bing Apps have been added (News, Weather, Sport, Health + fitness, Food + drink, Finance and Travel)</li>
<li>Podcasts has been added as a new default app</li>
<li>Sense apps: Power Sense, Data Sense and WiFi Sense have been added</li>
<li>Xbox Music has been added to replace Music + video</li>
<li>Xbox Video has been added to replace Music + video</li>
<li>Calendar
<ul>
<li>Support for Google Calendar</li>
<li>Shows weather in the calendar</li>
<li>Week view</li>
<li>Separated from the OS</li>
</ul>
</li>
<li>Call
<ul>
<li>In-call commands</li>
</ul>
</li>
<li>Camera
<ul>
<li>Burst mode</li>
<li>New design</li>
</ul>
</li>
<li>E-mail
<ul>
<li>Support for signing and encrypting mails</li>
<li>New options to sync mailes</li>
<li>Option to always download mails, including images</li>
</ul>
</li>
<li>Messaging
<ul>
<li>Conversations can now be set to no longer show notifications</li>
</ul>
</li>
<li>Office
<ul>
<li>Support for password protected files</li>
<li>Office Lens-support</li>
</ul>
</li>
<li>OneDrive
<ul>
<li>SkyDrive has been renamed to OneDrive</li>
<li>OneDrive now has an explorer for local files</li>
</ul>
</li>
<li>People
<ul>
<li>People can now be filteredby phone number</li>
</ul>
</li>
<li>Photos
<ul>
<li>Photos now can be marked favorits</li>
</ul>
</li>
<li>Waller
<ul>
<li>Support for tickets and memberships</li>
</ul>
</li>
</ul>
<h3>Developers</h3>
<ul>
<li>Apps can now record their own audio and video output</li>
<li>Apps and their save data can now be saved on SD-cards</li>
<li>Apps can now back-up their data to OneDrive</li>
<li>Background tasks</li>
<li>Bluetooth signal strength</li>
<li>Connectivity control</li>
<li>Location options</li>
<li>Chat log notifications</li>
<li>Push notifications</li>
<li>Rfcommconnections</li>
<li>System events</li>
<li>Timer</li>
