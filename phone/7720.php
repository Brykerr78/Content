<h2>What's new in Windows Phone 7.5 build 7720.68?</h2>
<h3>Interface</h3>
<ul>
<li>There is now a search option in the All apps list</li>
<li>You can now tab a letter to jump trough the list</li>
</ul>
<h3>Features</h3>
<ul>
<li>Voice-to-text and text-to-voice support</li>
<li>Visual voicemail support</li>
<li>Internet sharing</li>
<li>Battery saver</li>
<li>Ringtone management</li>
<li>Support for more emoticons</li>
<li>Shutting down the phone now requires you to slide your lockscreen down</li>
<li>"Soft on" has been removed</li>
</ul>
<h3>Internet Explorer</h3>
<ul>
<li>Internet Explorer has been updated to version 9</li>
<li>Improved supprot for HTML5 and CSS3</li>
<li>Gelocation support</li>
<li>New Chackra JavaScript engine</li>
<li>Revamped UI with the address bar on the bottom</li>
<li>Address bar is now still visible in landscape mode</li>
<li>Tabs is no longer a button in the UI, but in the menu</li>
<li>"Find on Page" has been removed</li>
</ul>
