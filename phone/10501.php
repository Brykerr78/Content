<h2>What's new in Windows Phone 8 build 10501?</h2>
<h3>Interface</h3>
<ul>
<li>Large displays can now show 3 medium tiles in width</li>
</ul>
<h3>Features</h3>
<ul>
<li>Driving Mode has been added to improve the smartphone usage when driving</li>
<li>New accessibility improvements</li>
<li>Bluetooth paring with Windows 8.1 devices</li>
<li>Customize the notification sounds</li>
<li>You can now lock your screen orientation</li>
<li>Apps can now be closed from the app switcher</li>
<li>The Wi-Fi will now be set up during the first run of the device</li>
</ul>
<h3>Internet Explorer</h3>
<ul>
<li>An issue with the viewport has been fixed</li>
</ul>
<h3>System technologies</h3>
<ul>
<li>Support for 1080p screen resolution</li>
<li>Support for new SoCs, like the Qualcomm Snapdragon 800</li>
<li>Improved storage management</li>
<li>Improved Bluetooth support</li>
</ul>
